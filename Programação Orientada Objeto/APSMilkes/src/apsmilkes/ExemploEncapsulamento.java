package apsmilkes;

public class ExemploEncapsulamento {

    private double racaoGato;
    private double racaoCachorro;
    private double brinquedos;
    private double arranhador;
    private double pipicat;

    public double getRacaoGato() {
        return racaoGato;
    }

    public void setRacaoGato(double racaoGato) {
        this.racaoGato = racaoGato;
    }

    public double getRacaoCachorro() {
        return racaoCachorro;
    }

    public void setRacaoCachorro(double racaoCachorro) {
        this.racaoCachorro = racaoCachorro;
    }

    public double getBrinquedos() {
        return brinquedos;
    }

    public void setBrinquedos(double brinquedos) {
        this.brinquedos = brinquedos;
    }

    public double getArranhador() {
        return arranhador;
    }

    public void setArranhador(double arranhador) {
        this.arranhador = arranhador;
    }

    public double getPipicat() {
        return pipicat;
    }

    public void setPipicat(double pipicat) {
        this.pipicat = pipicat;
    }

}
